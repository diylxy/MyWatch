//*********任意两点间画直线*********************************//
//x0、y0为起始点坐标，x1、y1为终点坐标**********************//
//**********************************************************//
void oled_draw_line(unsigned char x0 , unsigned char y0 , unsigned char x1 , unsigned char y1)
{
	uint8_t dx;//直线x轴差值
	uint8_t dy;//直线y轴差值
	uint8_t dx_sym;//x轴增长方向，为-1时减值方向，为1时增值方向
	uint8_t dy_sym;//y轴增长方向，为-1时减值方向，为1时增值方向
	uint8_t dx_x2;//dx*2值变量，用于加快运算速度
	uint8_t dy_x2;//dy*2值变量，用于加快运算速度
	uint8_t di;   //决策变量

	if(x0 == x1)//判断是否为垂直线
	{
		oled_drawvline(x0 , y0 , y1);//画垂直线
		return;
	}
	if(y0 == y1)//判断是否为水平线
	{
		oled_drawhline(x0 , x1 , y0);//画水平线
		return;
	}

	dx = x1 - x0;//求取两点之间的差值
	dy = y1 - y0;

	//****判断增长方向，或是否为水平线、垂直线、点*//
	if(dx > 0)//判断x轴方向
		dx_sym = 1;
	else
	{
		if(dx < 0)
			dx_sym = -1;
		else
		{
			oled_drawvline(x0 , y0 , y1);
			return;
		}
	}

	if(dy > 0)//判断y轴方向
		dy_sym = 1;
	else
	{
		if(dy < 0)
			dy_sym = -1;
		else
		{
			oled_drawvline(x0 , x1 , y0);
			return;
		}
	}


	/*将dx、dy取绝对值***********/
	dx = dx_sym * dx;
	dy = dy_sym * dy;

	/****计算2倍的dx、dy值*******/
	dx_x2 = dx * 1;//我改为了一倍，这样才跟真实的两点对应
	dy_x2 = dy * 1;

	/***使用bresenham法进行画直线***/
	if(dx >= dy)//对于dx>=dy，使用x轴为基准
	{
		di = dy_x2 - dx;
		while(x0 != x1)
		{
			oled_draw_dot(x0,y0);
			x0 +=dx_sym;
			if(di < 0)
				di += dy_x2;//计算出下一步的决策值
			else
			{
				di += dy_x2 - dx_x2;
				y0 += dy_sym;
			}	
		}
		oled_draw_dot(x0, y0);//显示最后一点
	}
	else  //对于dx<dy使用y轴为基准
	{
		di = dx_x2 - dy;
		while(y0 != y1)
		{
			oled_draw_dot(x0, y0);
			y0 += dy_sym;
			if(di < 0)
				di += dx_x2;
			else
			{
				di += dx_x2 - dy_x2;
				x0 += dx_sym;
			}
		}
		oled_draw_dot(x0, y0);//显示最后一点
	}
}

//////////////////////反白区域填充
void oled_fill_rect(int8_t x1, int8_t y1, int8_t x2, int8_t y2)
{
	int8_t y_start_full;						//完全填充开始
	int8_t y_end_full;							//完全填充截止
	uint8_t x, y;
	uint8_t tmp;	
	uint16_t buffer_pos;
	if((y1 > y2) || (x1 > x2))return;
	if((y1 == y2) && (x1 == x2))
	{
		oled_draw_dot(x1, y1);
		return;
	}
	y_start_full = y1/8;
	y1 = y1 %8;

	y_end_full = y2 /8;
	y2 = y2 %8;	
	
	if(y_start_full == y_end_full){	//只需改一页
		tmp = bitmap1[y2 - y1];
		tmp <<= y1;
		buffer_pos = y_end_full;
		if(buffer_pos == 8)buffer_pos--;
		buffer_pos *= 128;
		buffer_pos += x1;
		for(x = x1; x <= x2; ++x)
		{
			oled_buffer[buffer_pos] ^= tmp;
			buffer_pos++;
		}
		return;
	}
	
	if(y1 != 0)
	{
		tmp = ~(0xff >> (7-y1));
		buffer_pos = y_start_full;
		buffer_pos *= 128;
		buffer_pos += x1;
		for(x = x1; x <= x2; ++x)
		{
			oled_buffer[buffer_pos] ^= tmp;
			buffer_pos++;
		}
		y_start_full ++;	
	}
	
	if(y2 != 7)				//如果最后一页不是完整的
	{
		tmp = ~(0xff << (y2+1));
		buffer_pos = y_end_full;
		buffer_pos *= 128;
		buffer_pos += x1;
		for(x = x1; x <= x2; ++x)
		{
			oled_buffer[buffer_pos] ^= tmp;
			buffer_pos++;
		}
		y_end_full--;
	}
	if(y_start_full > y_end_full)return;
	//相同部分
	for(y = y_start_full; y <= y_end_full; ++y)
	{
		buffer_pos = y;
		buffer_pos *= 128;
		buffer_pos += x1;
		tmp = 0xff;
		for(x = x1; x <= x2; ++x)
		{
			oled_buffer[buffer_pos] ^= tmp;
			buffer_pos++;
		}
	}
}
