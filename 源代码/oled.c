void oled_draw_bitmap_fast(uint8_t x, uint8_t yy, uint8_t* bitmap, uint8_t w, uint8_t h, uint8_t invert, uint8_t offsetY)
{
	uint8_t ww, hh;
	uint8_t y = yy - offsetY;
	uint8_t pixels;
	uint8_t h2 = h / 8;

	uint8_t pixelOffset = (y % 8);
	uint8_t thing3 = (yy+h);
	
	// 
	for(hh = 0; hh < h2; ++hh)
	{
		// 
		uint8_t hhh = (hh * 8) + y; // Current Y pos (every 8 pixels)
		uint8_t hhhh = hhh + 8; // Y pos at end of pixel column (8 pixels)
		uint8_t offsetMask = 0xFF;
		uint16_t aa = ((hhh / 8) * OLED_X_MAX);
		
		const uint8_t* b = bitmap + (hh*w);
		
		// 
		if(offsetY && (hhhh < yy || hhhh > OLED_Y_MAX || hhh > thing3))
			continue;

		// 
		if(offsetY)
		{
			if(hhh < yy)
				offsetMask = (0xFF<<(yy-hhh));
			else if(hhhh > thing3)
				offsetMask = (0xFF>>(hhhh-thing3));
		}


		// If() outside of loop makes it faster (doesn't have to keep re-evaluating it)
		// Downside is code duplication
		if(!pixelOffset && hhh < OLED_Y_MAX)
		{
			// 
			for(ww = 0; ww < w; ++ww)
			{
				// Workout X co-ordinate in frame buffer to place next 8 pixels
				uint8_t xx = ww + x;
			
				// Stop if X co-ordinate is outside the frame
				if(xx >= OLED_X_MAX)
					continue;

				// Read pixels
				//uint8_t pixels = readPixels(b + ww, invert) & offsetMask;
				
				pixels = *(b + ww);
				if(invert)
					pixels = ~pixels;
				pixels &= offsetMask;
				
				oled_buffer[xx + aa] |= pixels;

				//setBuffuint8_t(buff, xx, hhh, pixels, colour);
			}
		}
		else
		{
			uint16_t aaa = ((hhhh / 8) * OLED_X_MAX);
			
			// 
			for(ww = 0; ww < w; ++ww)
			{
				// Workout X co-ordinate in frame buffer to place next 8 pixels
				uint8_t xx = ww + x;
		
				// Stop if X co-ordinate is outside the frame
				if(xx >= OLED_X_MAX)
					continue;

				// Read pixels
				pixels = *(b + ww);
				if(invert)
					pixels = ~pixels;
				pixels &= offsetMask;

				// 
				if(hhh < OLED_Y_MAX)
					oled_buffer[xx + aa] |= pixels << pixelOffset;
					//setBuffuint8_t(buff, xx, hhh, pixels << pixelOffset, colour);				

				// 
				if(hhhh < OLED_Y_MAX)
					oled_buffer[xx + aaa] |= pixels >> (8 - pixelOffset);
					//setBuffuint8_t(buff, xx, hhhh, pixels >> (8 - pixelOffset), colour);		
			}
		}
	}
}
