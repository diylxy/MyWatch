void oled_wr_cmd(uint8_t cmd)
{
	OLED_CS = 0;
	OLED_DC = 0;
	OLED_WR = 0;
	OLED_DATA = cmd;
	OLED_WR = 1;
	OLED_CS = 1;
}

void oled_wr_dat(uint8_t dat)
{
	OLED_CS = 0;
		OLED_DC = 1;
		OLED_WR = 0;
		OLED_DATA = dat;
		OLED_WR = 1;
	OLED_CS = 1;
}

void oled_send_buffer()
{
	uint8_t i ,j;

	for(i = 0;i != 8; ++i)
	{
		oled_wr_cmd(0xb0+i);		//page0-page1
		oled_wr_cmd(0x00);		//low column start address
		oled_wr_cmd(0x10);		//high column start address
		OLED_CS = 0;
		OLED_DC = 1;
		for(j = 0; j != 128; ++j)
		{
			OLED_WR = 0;
			OLED_DATA = oled_buffer[128*i+j];
			OLED_WR = 1;
		}
	}
	OLED_CS = 1;
}
void oled_clear_buffer()
{
	uint16_t i;
	for(i = 0; i != 1024; ++i)
	{
		oled_buffer[i] = 0;
	}
}

void oled_init_sprites()
{
	uint8_t i;
	
	for(i = 0; i != OLED_MAX_SPIRITS; ++i)
	{
		oled_sprite[i].x = oled_sprite[i].y = oled_sprite[i].now_x = oled_sprite[i].now_y = 64;
		oled_sprite[i].w = oled_sprite[i].h = 0;
		oled_sprite[i].enable= 0;
		oled_sprite[i].gooff=0;
		oled_sprite[i].speed=0;
	}
}
