void oled_sprite_change_str_gb2312(uint8_t num, uint8_t *str)
{
	uint8_t *str1 = str;
	uint16_t data_pos = 0;
	uint32_t foffset;
	uint8_t qh, ql;
	while(*str1)
	{
		qh=*str1++;
		ql=*str1++;
		if(qh < 0x80)
		{
			//ASCII码
			str1--;
			memcpy(oled_sprite[num].buffer + data_pos, oled_font_8x16 + (uint16_t)(qh-'!')*16, 8);
			data_pos += 8;
			if(data_pos >127)break;
			continue;
		}
		ql -= 0xa1;
		qh -= 0xa1;
		foffset = ((uint32_t)94*qh + ql)*(16 * 2) + FLASH_GB2312_START;
		flash_read(foffset, 16, oled_sprite[num].buffer + data_pos);
		data_pos += 16;
		if(data_pos > 127)break;
	}
	str1 = str;
	while(*str1)
	{
		qh=*str1++;
		ql=*str1++;
		if(qh < 0x80)
		{
			//ASCII码
			str1--;
			memcpy(oled_sprite[num].buffer + data_pos, oled_font_8x16 + (uint16_t)(qh-'!')*16 + 8, 8);
			data_pos += 8;
			if(data_pos > 255)break;
			continue;
		}
		ql -= 0xa1;
		qh -= 0xa1;
		foffset = ((uint32_t)94*qh + ql)*(16 * 2) + FLASH_GB2312_START + 16;
		flash_read(foffset, 16, oled_sprite[num].buffer + data_pos);
		data_pos += 16;
		if(data_pos > 255)break;
	}
	oled_sprite[num].w = data_pos / 2;
	oled_sprite[num].h = 16;
	oled_sprite[num].enable = 1;
}	

